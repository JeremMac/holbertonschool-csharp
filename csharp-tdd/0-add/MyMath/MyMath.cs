using System;

namespace MyMath
{
    /// <summary>
    ///A class that contains the Add method.
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// A method that adds two integers.
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
